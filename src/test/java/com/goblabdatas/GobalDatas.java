package com.goblabdatas;

public class GobalDatas {
	private String logusertoken;

	private int StateIdNumber;

	private String statevalue ;

	private int CityId;

	private String AddressId;
	
	private int statuscode;

	public String getLogusertoken() {
		return logusertoken;
	}

	public void setLogusertoken(String logusertoken) {
		this.logusertoken = logusertoken;
	}

	public int getStateIdNumber() {
		return StateIdNumber;
	}

	public void setStateIdNumber(int stateIdNumber) {
		StateIdNumber = stateIdNumber;
	}

	public String getStatevalue() {
		return statevalue;
	}

	public void setStatevalue(String statevalue) {
		this.statevalue = statevalue;
	}

	public int getCityId() {
		return CityId;
	}

	public void setCityId(int cityId) {
		CityId = cityId;
	}

	public String getAddressId() {
		return AddressId;
	}

	public void setAddressId(String addressId) {
		AddressId = addressId;
	}

	public int getStatuscode() {
		return statuscode;
	}

	public void setStatuscode(int statuscode) {
		this.statuscode = statuscode;
	}
	
}
